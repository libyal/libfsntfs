/* Tests the libfsntfs_fixup_values_apply function
 * Returns 1 if successful or 0 if not
 */
int fsntfs_test_fixup_values_apply(
     void )
{
	uint8_t mft_entry_data[ 1024 ];

	libcerror_error_t *error = NULL;
	void *memcpy_result      = NULL;
	int result               = 0;

	/* Initialize test
	 */
	memcpy_result = memory_copy(
	                 mft_entry_data,
	                 fsntfs_test_fixup_values_data1,
	                 sizeof( uint8_t ) * 1024 );

	FSNTFS_TEST_ASSERT_IS_NOT_NULL(
	 "memcpy_result",
	 memcpy_result );

	/* Test regular cases
	 */
	result = libfsntfs_fixup_values_apply(
	          mft_entry_data,
	          1024,
	          48,
	          3,
	          &error );

	FSNTFS_TEST_ASSERT_EQUAL_INT(
	 "result",
	 result,
	 1 );

	FSNTFS_TEST_ASSERT_IS_NULL(
	 "error",
	 error );

/* TODO check if fix-up values have been applied */

	/* Initialize test
	 */
	memcpy_result = memory_copy(
	                 mft_entry_data,
	                 fsntfs_test_fixup_values_data1,
	                 sizeof( uint8_t ) * 1024 );

	FSNTFS_TEST_ASSERT_IS_NOT_NULL(
	 "memcpy_result",
	 memcpy_result );

	/* Test error case where fix-up value is invalid
	 */
	byte_stream_copy_from_uint16_little_endian(
	 &( mft_entry_data[ 510 ] ),
	 0xffff );

	result = libfsntfs_fixup_values_apply(
	          mft_entry_data,
	          1024,
	          48,
	          3,
	          &error );

	byte_stream_copy_from_uint16_little_endian(
	 &( mft_entry_data[ 510 ] ),
	 0x0200 );

	FSNTFS_TEST_ASSERT_EQUAL_INT(
	 "result",
	 result,
	 1 );

	FSNTFS_TEST_ASSERT_IS_NULL(
	 "error",
	 error );

/* TODO check if fix-up values have been applied */

	/* Initialize test
	 */
	memcpy_result = memory_copy(
	                 mft_entry_data,
	                 fsntfs_test_fixup_values_data1,
	                 sizeof( uint8_t ) * 1024 );

	FSNTFS_TEST_ASSERT_IS_NOT_NULL(
	 "memcpy_result",
	 memcpy_result );

	/* Test error cases
	 */
	result = libfsntfs_fixup_values_apply(
	          NULL,
	          1024,
	          48,
	          3,
	          &error );

	FSNTFS_TEST_ASSERT_EQUAL_INT(
	 "result",
	 result,
	 -1 );

	FSNTFS_TEST_ASSERT_IS_NOT_NULL(
	 "error",
	 error );

	libcerror_error_free(
	 &error );

	result = libfsntfs_fixup_values_apply(
	          mft_entry_data,
	          (size_t) SSIZE_MAX + 1,
	          48,
	          3,
	          &error );

	FSNTFS_TEST_ASSERT_EQUAL_INT(
	 "result",
	 result,
	 -1 );

	FSNTFS_TEST_ASSERT_IS_NOT_NULL(
	 "error",
	 error );

	libcerror_error_free(
	 &error );

	result = libfsntfs_fixup_values_apply(
	          mft_entry_data,
	          1024,
	          0xffff,
	          3,
	          &error );

	FSNTFS_TEST_ASSERT_EQUAL_INT(
	 "result",
	 result,
	 -1 );

	FSNTFS_TEST_ASSERT_IS_NOT_NULL(
	 "error",
	 error );

	libcerror_error_free(
	 &error );

	result = libfsntfs_fixup_values_apply(
	          mft_entry_data,
	          1024,
	          48,
	          0xffff,
	          &error );

	FSNTFS_TEST_ASSERT_EQUAL_INT(
	 "result",
	 result,
	 -1 );

	FSNTFS_TEST_ASSERT_IS_NOT_NULL(
	 "error",
	 error );

	libcerror_error_free(
	 &error );

	return( 1 );

on_error:
	if( error != NULL )
	{
		libcerror_error_free(
		 &error );
	}
	return( 0 );
}

